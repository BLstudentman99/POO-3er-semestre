package Clases;

public class FormulaGeneral {
    private int a;
    private int b;
    private int c;
    
    public FormulaGeneral(int a, int b, int c){
        this.a = a;
        this.b = b;
        this.c = c;
    
    }
    public int geta(){
        return a;
}
    public void seta(int a){
        this.a = a;
    
    }
    public int getb(){
        return b;
}
    public void setb(int b){
        this.a = b;
    
    }
    public int getc(){
        return c;
}
    public void setc(int c){
        this.c = c;
    
    }
    
}
