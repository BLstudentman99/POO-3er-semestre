package Clases;

public class TrianguloRectangulo {
    private int base;
    private int altura;
    
    public TrianguloRectangulo(int base, int altura){
        this.base = base;
        this.altura = altura;
    }
    
    public int getBase(){
        return this.base;
    }
    public void setBase(int base){
        this.base = base;
    }
    public int getAltura(){
        return this.altura;   
    }
    public void setAltura(int altura){
        this.altura = altura;

    }
}

